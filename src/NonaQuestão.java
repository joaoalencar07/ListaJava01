public class NonaQuestão {
    public static void main(String[] args) {
        int numero1 = 1;
        int numero2 = 2;

        int sum = numero2 + numero1;
        int subtraction = numero2 - numero1;
        int multiplication = numero2 * numero1;
        int divider = numero2 / numero1;

        System.out.printf("Soma: %d, Subtração: %d, Divisão: %d, Multiplicação: %d\n", sum, subtraction, divider, multiplication);
    }
}